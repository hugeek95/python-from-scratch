import numpy as np

# Arreglo de lista
arr = np.array([1, 2, 3, 4, 5])
print(arr)

# Arreglo de ceros
print(np.zeros(5))   # [0. 0. 0. 0. 0.]

# Arreglo de unos
print(np.ones((2, 3)))  # matriz 2x3 llena de 1s

# Rango de números
print(np.arange(0, 10, 2))  # [0 2 4 6 8]

# Números equiespaciados
print(np.linspace(0, 1, 5))  # [0. 0.25 0.5 0.75 1.]pip install numpy

