# Conjuntos en Python
# Un conjunto es una colección desordenada de elementos únicos.
# Se definen usando llaves {} o la función set().
# Ejemplo de creación de conjuntos
conjunto_a = {1, 2, 3, 4, 5}
conjunto_b = set([4, 5, 6, 7, 8])
lista = [1, 2, 2, 3, 4, 4, 5]
print("Conjunto A:", conjunto_a)
print("Conjunto B:", conjunto_b)
print(type(conjunto_a))  # <class 'set'>
print(type(lista))