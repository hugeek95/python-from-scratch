# 1. Enteros (int)
edad = 25
temperatura = 36.6
print(f"Edad: {edad}, Temperatura: {temperatura}")

# 2. Flotantes (float)
precio = 19.99
altura = 1.75
print(f"Precio: {precio}, Altura: {altura}")

# 3. Cadenas de texto (str)
nombre = "Juan"
saludo = "Hola, ¿cómo estás?"
print(f"Nombre: {nombre}, Saludo: {saludo}")
# 4. Booleanos (bool)
es_estudiante = True
tiene_licencia = False
print(f"Es estudiante: {es_estudiante}, Tiene licencia: {tiene_licencia}")
# 5. None (NoneType)
valor_nulo = None
print(f"Valor nulo: {valor_nulo}")